async function main() {
  console.log('🚀 Starting game database seeding...\n');

  try {
    // ============ CLEAN EXISTING DATA (Optional) ============
    console.log('STEP 0: Cleaning existing GameGenre collection...');
    await prisma.gameGenre.deleteMany({});
    console.log('✅ Cleared GameGenre collection\n');

    // ============ SEED GENRES ============
    console.log('STEP 1/3: Seeding genres...');
    for (const genre of genres) {
      await prisma.genre.upsert({
        where: { slug: genre.slug },
        update: { name: genre.name },
        create: {
          id: genre.id,
          name: genre.name,
          slug: genre.slug,
        },
      });
    }
    console.log(`✅ Imported ${genres.length} genres\n`);

    // ============ SEED GAMES ============
    console.log('STEP 2/3: Seeding games...');
    let gameCount = 0;
    let skippedCount = 0;

    for (const game of games) {
      try {
        const existingGame = await prisma.game.findUnique({
          where: { slug: game.slug },
        });

        if (existingGame) {
          console.log(`  ⏭️  Skipping ${game.slug} (already exists)`);
          skippedCount++;
          continue;
        }

        // Create the game
        await prisma.game.create({
          data: {
            slug: game.slug,
            name: game.name,
            description: game.description,
            released: game.released,
            rating: game.rating,
            ratingsCount: game.ratingsCount,
            metacritic: game.metacritic,
            platform: ['Windows', 'PlayStation', 'Xbox', 'Nintendo Switch'],
            tags: game.tags,
            backgroundImage: `/images/${game.slug}.jpg`,
          },
        });

        gameCount++;
        console.log(`  ✅ Added ${game.slug} → /images/${game.slug}.jpg`);
      } catch (error) {
        console.error(
          `  ❌ Error adding ${game.slug}:`,
          error instanceof Error ? error.message : String(error),
        );
      }
    }
    console.log(
      `\n✅ Imported ${gameCount} new games (skipped ${skippedCount} existing)\n`,
    );

    // ============ SEED GAME GENRE RELATIONSHIPS ============
    console.log('STEP 3/3: Creating GameGenre relationships...');
    let relationCount = 0;
    let skippedRelationCount = 0;

    for (const game of games) {
      try {
        // Find the game
        const dbGame = await prisma.game.findUnique({
          where: { slug: game.slug },
        });

        if (!dbGame) {
          console.log(`  ⏭️  Game ${game.slug} not found, skipping relations`);
          skippedRelationCount++;
          continue;
        }

        // Create genre relationships for this game
        for (const genreId of game.genreIds) {
          // Check if relation already exists
          const existingRelation = await prisma.gameGenre.findUnique({
            where: {
              gameId_genreId: {
                gameId: dbGame.id,
                genreId: genreId,
              },
            },
          });

          if (!existingRelation) {
            await prisma.gameGenre.create({
              data: {
                gameId: dbGame.id,
                genreId: genreId,
              },
            });
            relationCount++;
          }
        }

        if (game.genreIds.length > 0) {
          console.log(
            `  🔗 Added ${game.genreIds.length} genre relations for ${game.slug}`,
          );
        }
      } catch (error) {
        console.error(
          `  ❌ Error creating relations for ${game.slug}:`,
          error instanceof Error ? error.message : String(error),
        );
      }
    }

    console.log(
      `\n✅ Created ${relationCount} new GameGenre relationships (skipped ${skippedRelationCount})`,
    );

    // ============ SHOW STATS ============
    const totalGames = await prisma.game.count();
    const totalGenres = await prisma.genre.count();
    const totalGameGenres = await prisma.gameGenre.count();

    console.log(`\n📊 DATABASE STATISTICS:`);
    console.log(`   • Games: ${totalGames}`);
    console.log(`   • Genres: ${totalGenres}`);
    console.log(`   • Game-Genre Relations: ${totalGameGenres}`);
    console.log(`\n🎉 SEEDING COMPLETE!`);
  } catch (error) {
    console.error('❌ Seeding failed:', error);
  } finally {
    await prisma.$disconnect();
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
